export type UiThemeTokens = {
  background: string;
  foreground: string;
  card: string;
  cardForeground: string;
  popover: string;
  popoverForeground: string;
  primary: string;
  primaryForeground: string;
  secondary: string;
  secondaryForeground: string;
  muted: string;
  mutedForeground: string;
  accent: string;
  accentForeground: string;
  destructive: string;
  destructiveForeground: string;
  border: string;
  input: string;
  ring: string;
};

export type UiThemePreset = {
  id: string;
  name: string;
  collection?: "core" | "copilot";
  preview?: {
    sidebar: string;
    activity: string[];
    syntax: string[];
  };
  tokens: UiThemeTokens;
};

type CopilotThemeConfig = {
  id: string;
  name: string;
  background: string;
  foreground: string;
  card: string;
  secondary: string;
  muted: string;
  mutedForeground: string;
  border: string;
  primary: string;
  added: string;
  removed: string;
  skill: string;
};

const createCopilotLightTheme = (config: CopilotThemeConfig): UiThemePreset => ({
  id: config.id,
  name: config.name,
  collection: "copilot",
  preview: {
    sidebar: config.secondary,
    activity: [config.removed, config.skill, config.added, config.primary],
    syntax: [config.primary, config.removed, config.added, config.skill],
  },
  tokens: {
    background: config.background,
    foreground: config.foreground,
    card: config.card,
    cardForeground: config.foreground,
    popover: config.card,
    popoverForeground: config.foreground,
    primary: config.primary,
    primaryForeground: "0 0% 100%",
    secondary: config.secondary,
    secondaryForeground: config.foreground,
    muted: config.muted,
    mutedForeground: config.mutedForeground,
    accent: config.primary,
    accentForeground: "0 0% 100%",
    destructive: config.removed,
    destructiveForeground: "0 0% 100%",
    border: config.border,
    input: config.border,
    ring: config.primary,
  },
});

